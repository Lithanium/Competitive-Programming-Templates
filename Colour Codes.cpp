#define YELLOW  "\e[0;33m"
#define BLUE    "\e[0;34m"
#define PURPLE  "\e[0;35m"
#define CYAN    "\e[0;36m"
#define WHITE   "\e[0;37m"

/* BACKGROUND COLOURS */
#define BGBLACK  "\e[40m"
#define BGRED    "\e[41m"
#define BGGREEN  "\e[42m"
#define BGYELLOW "\e[43m"
#define BGBLUE   "\e[44m"
#define BGPURPLE "\e[45m"
#define BGCYAN   "\e[46m"

